"""
Camel Case
Each word, except the first, starts with a capital letter:
"""
myVariableName = "John";
print("Camel Case - myVariableName : ", myVariableName);

"""
Pascal Case
Each word starts with a capital letter:
"""
MyVariableName = "John"
print("Pascal Case - MyVariableName : ", MyVariableName);

"""
Snake Case
Each word is separated by an underscore character:
"""
my_variable_name = "John"
print("Snake Case - my_variable_name : ", my_variable_name);