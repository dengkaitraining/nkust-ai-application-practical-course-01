"""
Python - String Concatenation
String Concatenation
To concatenate, or combine, two strings you can use the + operator.
"""
# Ex1. Merge variable a with variable b into variable c:
print("Ex1. Merge variable a with variable b into variable c:")
a = "Hello"
b = "World"
c = a + b
print("a length: " + str(len(a)) + ", b length: " + str(len(b)) + ", c length: " + str(len(c)))
print("Use the c = a + b statement: " + c)
print("")

# Ex2. To add a space between them, add a " ":
print("Ex2. To add a space between them, add a ' ':")
a = "Hello"
b = "World"
c = a + " " + b
print("a length: " + str(len(a)) + ", b length: " + str(len(b)) + ", c length: " + str(len(c)))
print("Use the c = a + ' ' + b statement: " + c)
print("")
