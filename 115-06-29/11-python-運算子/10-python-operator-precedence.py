"""
Operator Precedence
Operator precedence describes the order in which operations are performed.
"""
# Ex1. Parentheses has the highest precedence, meaning that expressions inside parentheses must be evaluated first:
print("Ex1. Parentheses has the highest precedence, meaning that expressions inside parentheses must be evaluated first:")
print("(6 + 3) - (6 + 3) = " , (6 + 3) - (6 + 3))
print("")

# Ex2. Multiplication * has higher precedence than addition +, and therefore multiplications are evaluated before additions:
print("Ex2. Multiplication * has higher precedence than addition +, and therefore multiplications are evaluated before additions:")
print("100 + 5 * 3 = " , 100 + 5 * 3)
print("")
