# Ex1. Here is an example using different arithmetic operators:
print("Ex1. Here is an example using different arithmetic operators:")
x = 15
y = 4

print("x + y = " + str(x + y))
print("x - y = " + str(x - y))
print("x * y = " + str(x * y))
print("x / y = " + str(x / y))
print("x % y = " + str(x % y))
print("x ** y = " + str(x ** y))
print("x // y = " + str(x // y))
print("")

"""
Division in Python
Python has two division operators:

/ - Division (returns a float)
// - Floor division (returns an integer)
"""
# Ex2. Division always returns a float:
print("Ex2. Division always returns a float:")
x = 12
y = 5

print("x / y = " + str(x / y))
print("")

# Ex3. Floor division always returns an integer. It rounds DOWN to the nearest integer:
print("Ex3. Floor division always returns an integer. It rounds DOWN to the nearest integer:")
x = 12
y = 5

print("x // y = " + str(x // y))
print("")