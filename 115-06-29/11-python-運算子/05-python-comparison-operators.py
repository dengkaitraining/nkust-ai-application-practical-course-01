# Ex1. Comparison operators return True or False based on the comparison:
print("Ex1. Comparison operators return True or False based on the comparison:")
x = 5
y = 3

print("x == y: " + str(x == y))
print("x != y: " + str(x != y))
print("x > y: " + str(x > y))
print("x < y: " + str(x < y))
print("x >= y: " + str(x >= y))
print("x <= y: " + str(x <= y))
print("")

"""
Chaining Comparison Operators
Python allows you to chain comparison operators:
"""
# Ex2. Chaining comparison operators:
print("Ex2. Chaining comparison operators:")
x = 5

print("1 < x < 10: " + str(1 < x < 10))
print("1 < x and x < 10: " + str(1 < x and x < 10))
print("")
