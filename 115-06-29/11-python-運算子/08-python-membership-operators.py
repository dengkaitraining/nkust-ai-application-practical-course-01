# Ex1. Check if "banana" is present in a list:
print("Ex1. Check if \"banana\" is present in a list:")
fruits = ["apple", "banana", "cherry"]
print("\"banana\" in fruits : " , "banana" in fruits)
print("")

# Ex2. Check if "pineapple" is NOT present in a list:
print("Ex2. Check if \"pineapple\" is NOT present in a list:")
fruits = ["apple", "banana", "cherry"]
print("\"pineapple\" not in fruits : ", "pineapple" not in fruits)
print("")

